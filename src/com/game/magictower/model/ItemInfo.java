package com.game.magictower.model;

public class ItemInfo {
    
    private int level;
    
    private int hp;
    
    private int money;
    
    private int attack;
    
    private int defend;
    
    private int ykey;
    
    private int bkey;
    
    private int rkey;
    
    private String describe;
    
    public ItemInfo() {
        
    }
    
    public int getLevel() {
        return level;
    }
    
    public int getHp() {
        return hp;
    }
    
    public int getMoney() {
        return money;
    }
    
    public int getAttack() {
        return attack;
    }
    
    public int getDefend() {
        return defend;
    }
    
    public int getYKey() {
        return ykey;
    }
    
    public int getBKey() {
        return bkey;
    }
    
    public int getRKey() {
        return rkey;
    }
    
    public String getDescribe() {
        return describe;
    }

}
